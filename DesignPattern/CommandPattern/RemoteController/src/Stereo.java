public class Stereo {
    public void on() {
        System.out.println("오디오 on");
    }
    public void off() {
        System.out.println("오디오 off");
    }
    public void setCd() {
        System.out.println("CD를 세팅합니다.");
    }
    public void setDvd() {
        System.out.println("DVD를 세팅합니다.");
    }
    public void setRadio() {
        System.out.println("라디오를 세팅합니다.");
    }
    public void setVolume(int num) {
        System.out.println("볼륨을 " + num + "으로 조절합니다.");
    }
}
